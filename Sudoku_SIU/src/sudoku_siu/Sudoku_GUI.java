    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        defaultSudoku = tableToSudoku();

        if (!Arrays.deepEquals(defaultSudoku, emptySudoku)) 
        {
            if (Arrays.deepEquals(defaultSudoku, tempSudoku)) 
            {
                JOptionPane.showMessageDialog(this, "Es Correcto!");
            } 
            else 
            {
                JOptionPane.showMessageDialog(this, "Te has equivocado :(");
            }

            //cambia colores segn est bien o mal
            for (int i = 0; i < defaultSudoku.length; i++) 
            {
                for (int j = 0; j < defaultSudoku.length; j++) 
                {
                    if (defaultSudoku[i][j] == tempSudoku[i][j]
                            && table[i][j].getBackground() != Color.LIGHT_GRAY) 
                    {
                        table[i][j].setBackground(Color.GREEN);
                    } 
                    else if (defaultSudoku[i][j] != tempSudoku[i][j]) 
                    {
                        table[i][j].setBackground(Color.RED);
                    }
                }
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

