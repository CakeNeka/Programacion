    static void fillTable()
    {
        for (int i = 0; i < table.length; i++) 
        {
            for (int j = 0; j < table[i].length; j++) 
            {
                table[i][j].setText(Integer.toString(defaultSudoku[i][j]));
                if (table[i][j].getText().equals("0"))
                {
                    table[i][j].setText("");
                    table[i][j].setEditable(true);
                    table[i][j].setBackground(Color.WHITE);
                    
                }
                else
                {
                    table[i][j].setBackground(Color.LIGHT_GRAY);
                    table[i][j].setEditable(false);
                }
            }
        }
    }
    
    static void resetTable()
    {
        for (int i = 0; i < table.length; i++) 
        {
            for (int j = 0; j < table[i].length; j++)
            {
                table[i][j].setText("");
                table[i][j].setEditable(false);
                
            }    
        }
    }
    
    static void resetSudoku()
    {
        tempSudoku = cloneSudokus(defaultSudoku);
        for (int i = 0; i < defaultSudoku.length; i++) 
        {
            for (int j = 0; j < defaultSudoku[i].length; j++)
            {
                defaultSudoku[i][j] = 0;
            }    
        }
    }
    
    static void newSudoku()
    {
        defaultSudoku = Sudoku_SIU.generateSudoku();
        tempSudoku = cloneSudokus(defaultSudoku);
    }
    
    static void createPuzle(int gaps)
    {
        Random random = new Random();
        tempSudoku = cloneSudokus(defaultSudoku);
        int[][] empty = new int[9][9];                  //para no vaciar dos veces el mismo hueco
       
        for (int i = 0; i < gaps; i++) 
        {
            int rowIndex;
            int colIndex;
            
            do
            {
                rowIndex = random.nextInt(9);
                colIndex = random.nextInt(9);
            } while (empty[rowIndex][colIndex] == 1);
            
            empty[rowIndex][colIndex] = 1;
            
            defaultSudoku[rowIndex][colIndex] = 0;
        }

    }
    
    static int[][] tableToSudoku()
    {
        int[][] userSudoku = new int[9][9];
        for (int i = 0; i < table.length; i++) 
        {
            for (int j = 0; j < table[i].length; j++) 
            {
                if(!table[i][j].getText().equals(""))
                    userSudoku[i][j] = Integer.parseInt(table[i][j].getText());
                else
                    userSudoku[i][j] = 0;
            }
        }
        
        return userSudoku;
    }
    
