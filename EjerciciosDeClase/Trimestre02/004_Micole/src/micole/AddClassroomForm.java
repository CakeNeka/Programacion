    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String id = classroomIdTf.getText().trim();
        int capacity = capacitySlider.getValue();
        int schoolIndex = schoolsCb.getSelectedIndex();
        
        if (id.isEmpty() || schoolIndex == -1){
            JOptionPane.showMessageDialog(this, "Please enter all fields");
        } else {
            School school = schools.get(schoolIndex);
            classrooms.add(new Classroom(id,capacity,school));
            saveClassroomsToFile();
        }
        
    }//GEN-LAST:event_saveBtnActionPerformed

