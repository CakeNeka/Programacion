    private void saveClassroomsToFile() {
        try {
            FileOutputStream file = new FileOutputStream("Classrooms.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);

            for (int i = 0; i < classrooms.size(); i++) {
                outputFile.writeObject(classrooms.get(i));
            }
            
            outputFile.close();
            JOptionPane.showMessageDialog(this, "Succesfuly saved");
            this.dispose();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void populateArrayList() {
        try {
            FileInputStream file2 = new FileInputStream("Schools.dat");
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);

            boolean endOfFile = false;
                while (!endOfFile) {
                try {
                    schools.add((School) inputFile2.readObject());
                } catch (EOFException e) //END OF FILE exception
                {
                    endOfFile = true;
                } catch (Exception f) {
                    JOptionPane.showMessageDialog(this, f.getMessage());
                }
            }
            inputFile2.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        try {
            FileInputStream file2 = new FileInputStream("Classrooms.dat");
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);

            boolean endOfFile = false;
            while (!endOfFile) {
                try {
                    classrooms.add((Classroom) inputFile2.readObject());
                } catch (EOFException e) //END OF FILE exception
                {
                    endOfFile = true;
                } catch (Exception f) {
                    JOptionPane.showMessageDialog(this, f.getMessage());
                }
            }
            inputFile2.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    
