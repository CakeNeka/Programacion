    private String[][] fetchSqlTable(String tableName) throws SQLException {
        executeSelect(tableName);
        String[][] table = createTableHeader();
        resultSet.beforeFirst();
        int i = 1;
        while (resultSet.next()) {
            for (int j = 0; j < table[i].length; j++) {
                if (resultSet.getObject(j + 1) == null)
                    table[i][j] = "null";
                else
                    table[i][j] = resultSet.getObject(j + 1).toString();
            }
            i++;
        }
        return table;
    }

    private void executeSelect(String table) throws SQLException {
        String query = "SELECT * FROM " + table;
        Statement stmt = connection.prepareStatement(query, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
        resultSet = stmt.executeQuery(query);
    }

    private String[][] createTableHeader() throws SQLException {
        ResultSetMetaData meta = resultSet.getMetaData();
        int columns = meta.getColumnCount();
        resultSet.last();
        int rows = resultSet.getRow();
        String[][] table = new String[rows + 1][columns];

        for (int i = 0; i < columns; i++) {
            table[0][i] = meta.getColumnName(i + 1) + "[" + meta.getColumnTypeName(i + 1) + "]";
        }

        return table;
    }

    private void showTable(String[][] table) {
        for (String[] strings : table) {
            for (String string : strings) {
                jTextArea1.append(String.format("%-30s", string));
            }
            jTextArea1.append("\n");
        }
    }
    
    private void fillJTable(String[][] table) {
        DefaultTableModel model = new DefaultTableModel(table[0], 0);
        for (int i = 1; i < table.length; i++) {
            model.addRow(table[i]);
        }
        jTable1.setModel(model);
    }
    
